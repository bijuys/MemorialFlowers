                                          <div class="clav_toggle_content">
                                            <p>Traditionally, flowers are a way to represent growth, new life and movement forward. The natural beauty of flowers at a funeral and at the home of mourners brings a sense of warmth and comfort to the environment.<br/>
Today, flowers are not mandatory but they are one way people express their love for the deceased and concern for members of the family. Flowers for a funeral should arrive at the funeral home before the first visiting hours - to be there when the family arrives. If time does not permit delivery before visiting hours, flowers or a plant can be sent to the home of the bereaved. A potted plant has obvious symbolic meaning because it will continue to live and grow.
</p>
                                            <p>There are instances when flowers are not appropriate. Such as when the family requests that donations be made in lieu of flowers. Although flowers are freely accepted by many religions and cultures at funerals, it is worth remembering that there are some which do not traditionally receive flowers such as the Jewish and Islamic faiths.<br/>
Jewish law has always demanded immediate burial – within three days – so flowers were never deemed necessary. To this day it has never been customary to send any flowers, although they are not forbidden and some Jews have begun sending them for Reformed Jewish funerals. Instead it is customary to send fruit and food baskets to the home of the bereaved during the mourning period. See our collection of <u>Sympathy Baskets</u>.
</p>
                                            <p>At Islamic funerals some people send flowers and some do not. It is, however, common to place individual flowers on graves along with palm branches and other greenery. Flowers are not a traditional part of Hindu funerals, but they are not unwelcome.</p>
